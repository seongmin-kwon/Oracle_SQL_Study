--각 데이터 베이스 제품들에는 자신의 데이터 베이스를 관리 할 수 있는 DBMS가존재합니다.
-- 특히 현재 사용중인 오라클 데이터 베이스에서 사용가능한 DBMS는 SQL PLUS,이클립스,오라클 DBMS등이 있다.
--데이터 베이스 활용분야: 일반웹사이트 게시문 저장용, 포털검색 사이트 검색 대상 정보 저장용
